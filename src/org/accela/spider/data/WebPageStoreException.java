package org.accela.spider.data;

public class WebPageStoreException extends Exception
{
	private static final long serialVersionUID = 1L;
	
	public WebPageStoreException()
	{
		super();
	}

	public WebPageStoreException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public WebPageStoreException(String message)
	{
		super(message);
	}

	public WebPageStoreException(Throwable cause)
	{
		super(cause);
	}
	
}
